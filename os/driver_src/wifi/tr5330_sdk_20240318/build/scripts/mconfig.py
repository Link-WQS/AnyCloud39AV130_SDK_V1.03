import os
import argparse

# from typing import Optional, Tuple, List

from kconfiglib import Kconfig
from menuconfig import menuconfig

class Chdir:
    """便利的切换工作目录
    """

    def __init__(self, work_dir: str):
        self._old_dir = os.getcwd()
        self._work_dir = os.path.abspath(work_dir)

    def __enter__(self):
        os.chdir(self._work_dir)
    
    def __exit__(self, exc_type, exc_val, exc_tb):
        os.chdir(self._old_dir)

# def environ_config(defconfig_path: Optional[str], title: str):
def environ_config(defconfig_path, title):
    '''配置menuconfig相关环境变量'''
    # 设置menuconfig界面
    os.environ["MENUCONFIG_STYLE"] = "default selection=fg:white,bg:blue"

    # 设置配置文件的头部文字
    os.environ["KCONFIG_CONFIG_HEADER"] = "# Generate by menuconfig\n\n"

    # 设置生成的配置文件名
    os.environ["KCONFIG_CONFIG"] = defconfig_path if defconfig_path else ".config"

    # 设置生成的配置项前缀
    os.environ["CONFIG_"] = ""

    # 设置menuconfig标题
    os.environ["WSCFG_MENUCONFIG_TITLE"] = title

# def parse_option_expr(option_expr: str)->Tuple[str, str]:
def parse_option_expr(option_expr):
    ol = option_expr.split('=')
    if len(ol) != 2:
        raise ValueError("Option expr `{}` is invalid!".format(option_expr))
    return ol[0].strip(), ol[1].strip()

# def mconfig(Kconfig_path: str, defconfig_path: str, autoheader: Optional[str], update_options: Optional[List[str]],
def mconfig(Kconfig_path: str, defconfig_path: str, autoheader, update_options,
        sync=False, title='', output_file=None):
    environ_config(output_file if output_file else defconfig_path, title)
    with Chdir(os.path.dirname(Kconfig_path)):
        kconf = Kconfig(filename=os.path.basename(Kconfig_path))
        if os.path.isfile(defconfig_path):
            kconf.load_config(defconfig_path)

        if autoheader:
            # generate a header file
            print(kconf.write_autoconf(autoheader))
        elif update_options:
            # update options
            for name, value in (parse_option_expr(t) for t in update_options):
                flag = False
                for sym in kconf.unique_defined_syms:
                    if name == sym.name:
                        flag = True
                        ret = sym.set_value(value)
                        if not ret:
                            print('[-] Failed to set the value of option `{}` to `{}`, check its dependencies or format!'.format(str(name), str(value)))
                            exit(1)
                        break
                if not flag:
                    print('[-] `{}` configuration item was not found!'.format(str(name)))
                    exit(1)
            print(kconf.write_config())
        elif sync:
            # sync defaults
            print(kconf.write_config())
        else:
            # Open the graphical menuconfig interface
            menuconfig(kconf)


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Generate config")
    parser.add_argument('kconfig_path', help="A Kconfig path")
    parser.add_argument('defconfig_path', help="The defconfig configuration path generated by menuconfig.")
    parser.add_argument('-a', '--autoheader', 
        help="Specify and generate the header file corresponding to the configuration.")
    parser.add_argument('-s', '--sync', 
        help="Synchronize kconfig configuration.", action='store_true')
    parser.add_argument('-u', '--update-options', 
        help="Update menuconfig configuration options like WSCFG_XXX=y", nargs='+')
    parser.add_argument('-t', '--title', 
        help="Specifies the menu header display.", default="TR5330 configuration")
    parser.add_argument('-o', '--output-file', 
        help="Specify the saved file path.")

    args = parser.parse_args()

    mconfig(args.kconfig_path, args.defconfig_path, args.autoheader, args.update_options, 
        args.sync, args.title, args.output_file)