void (*jumpto_L2bufexe)(void);


int l2_exec_buf(const char *vaddr, int len)
{
    unsigned long i ;

    memset((void *)0x48000000, 0, len);

    /* copy from ddr2 to l2 memory */
    for(i = 0; i < len; i += 4) {
        *(volatile unsigned long *)(0x48000000 + i) =
            *(unsigned long *)(vaddr + i);
    //  pr_err("[%s %d]:0x%x\n",__func__,__LINE__,
    //*(volatile unsigned long *)(0x48000000 + i));
    }

    /* disable ahb_flag_en */
    *(volatile unsigned long *)(0x20140000 + 0x84) &= ~(1 << 29);
    /* jumpto_L2 run */
    jumpto_L2bufexe = (void *)(0x48000000);
    jumpto_L2bufexe();

    return 0;
 }


